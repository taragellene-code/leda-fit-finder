export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { to, scholarName, topTerms, important, somewhat, niceToHave, notes } = req.body;

  if (!to || !to.includes("@")) {
    return res.status(400).json({ error: "Valid email required" });
  }

  const apiKey = process.env.RESEND_API_KEY;
  if (!apiKey) {
    return res.status(500).json({ error: "Email service not configured" });
  }

  const fromEmail = process.env.FROM_EMAIL || "LEDA College Fit Finder <onboarding@resend.dev>";

  // Build HTML email
  let html = `
    <div style="font-family: Helvetica, Arial, sans-serif; max-width: 600px; margin: 0 auto; color: #1a1a1a;">
      <div style="text-align: center; padding: 24px 0; border-bottom: 2px solid #B5282C;">
        <h1 style="font-size: 22px; margin: 0;">🧭 ${scholarName || "Your"}'s College Fit Profile</h1>
        <p style="color: #6b7280; font-size: 14px; margin: 8px 0 0;">Generated by LEDA College Fit Finder</p>
      </div>
  `;

  if (topTerms && topTerms.length > 0) {
    html += `
      <div style="background: #FEF2F2; border: 2px solid #B5282C; border-radius: 12px; padding: 20px; margin: 24px 0;">
        <h2 style="font-size: 18px; color: #8B1F22; margin: 0 0 12px;">🎯 Your Top 3 Priorities</h2>
    `;
    topTerms.forEach((term, i) => {
      html += `<div style="background: white; padding: 10px 16px; border-radius: 8px; margin: 6px 0; font-size: 15px;"><strong style="color: #B5282C; margin-right: 8px;">${i + 1}.</strong> ${term}</div>`;
    });
    html += `</div>`;
  }

  if (notes && notes.length > 0) {
    html += `<h3 style="font-size: 16px; margin: 24px 0 8px;">💬 Your Additional Notes</h3>`;
    notes.forEach(n => {
      html += `<p style="font-size: 12px; text-transform: uppercase; color: #6b7280; font-weight: 600; margin: 12px 0 2px;">${n.section}</p>`;
      html += `<p style="font-style: italic; color: #374151; margin: 0;">&ldquo;${n.comment}&rdquo;</p>`;
    });
  }

  if (important && important.length > 0) {
    html += `<h3 style="font-size: 16px; color: #8B1F22; margin: 24px 0 8px;">🔴 Important</h3>`;
    important.forEach(t => {
      html += `<div style="background: #F3F4F6; padding: 8px 14px; border-radius: 8px; margin: 4px 0; font-size: 14px;">${t}</div>`;
    });
  }

  if (somewhat && somewhat.length > 0) {
    html += `<h3 style="font-size: 16px; color: #E8852D; margin: 24px 0 8px;">🟠 Somewhat Important</h3>`;
    somewhat.forEach(t => {
      html += `<div style="background: #F3F4F6; padding: 8px 14px; border-radius: 8px; margin: 4px 0; font-size: 14px;">${t}</div>`;
    });
  }

  if (niceToHave && niceToHave.length > 0) {
    html += `<h3 style="font-size: 16px; margin: 24px 0 8px;">🟢 Nice to Have</h3>`;
    niceToHave.forEach(t => {
      html += `<div style="background: #F3F4F6; padding: 8px 14px; border-radius: 8px; margin: 4px 0; font-size: 14px;">${t}</div>`;
    });
  }

  html += `
      <div style="margin-top: 32px; padding-top: 16px; border-top: 1px solid #E5E7EB; text-align: center;">
        <p style="font-size: 12px; color: #9CA3AF;">Leadership Enterprise for a Diverse America (LEDA)</p>
      </div>
    </div>
  `;

  try {
    const response = await fetch("https://api.resend.com/emails", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        from: fromEmail,
        to: [to],
        subject: `${scholarName || "Your"}'s College Fit Profile — LEDA`,
        html: html,
      }),
    });

    const data = await response.json();

    if (!response.ok) {
      console.error("Resend error:", data);
      return res.status(500).json({ error: "Failed to send email", details: data });
    }

    return res.status(200).json({ success: true });
  } catch (err) {
    console.error("Email send error:", err);
    return res.status(500).json({ error: "Failed to send email" });
  }
}
